let async = require('async');
console.time('cost');
async.parallel([
  function(cb){
    setTimeout(function(){
      console.log(1);
      cb(null,'1');
    },2000)
  },
  function(cb){
    setTimeout(function(){
      console.log(2);
      cb(null,'2');
    },3000)
  },
  function(cb){
    setTimeout(function(){
      console.log(3);
      cb('我错了','3');
    },1000)
  }
],function(err,result){
  console.log(err);
  console.log(result);
  console.timeEnd('cost');
});